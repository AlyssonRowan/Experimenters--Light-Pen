;------------------------
;  FILL SCREEN (GRAPHICS MODE)
;     THIS ROUTINE WILL TURN ON EVERY PIXEL ON THE SCREEN
;
;   FOR USE BY BASIC PROGRAMS ONLY.  NO CALLING ARGUMENTS.
;-------------------------
CDSEG	SEGMENT
	ASSUME	CS:CDSEG
FLSCRG	PROC	FAR
	PUSH	BP
	MOV	BP,SP
;
	PUSH	ES
	PUSH	DI
	MOV	AX,0B800H	;  BASE ADDR OF DISPLAY BUFFER
	MOV	ES,AX
	XOR	AX,AX
	MOV	DI,AX		;  SET BEGINING OFFSET VALUE(0)
	MOV	CX,8192		;  NO. OF WORDS IN BUFFER TO FILL
	MOV	AX,0FFFFH	;  FILL VALUE = 1111111111111111B
	REP	STOSW		;  STORE FILL VALUE UNTIL CX=0
;
	POP	DI
	POP	ES
	POP	BP
	RET
FLSCRG	ENDP
CDSEG	ENDS
	END
